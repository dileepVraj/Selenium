package xpath.XpathAxes;

public class Intro
{
    /**
     * Xpath axes are used in Xpath expressions to navigate through the hierarchical structure of an XML or HTML
     * document.
     * They allow you to select elements based on their relationship to other elements in the document, rather than just
     * their name or attributes.
     * There are several axes used in Xpath, each with a different meaning and purpose.
     */

    /**
     *1. Ancestor axis(ancestor::) This axis selects all ancestors (parent,grandparent,etc) of current element.
     */

    /**
     * 2.Descendant axis(descendant::) This axis selects all descendants (children,grandChildren,etc) of current element.
     *
     */

    /**
     * 3.Parent axis(parent::)This axis selects the parent of the current element.
     */

    /**
     * 4.Child axis(child::) This axis selects all children of the current element.
     */

    /**
     * 5.Following-sibling axis(following-sibling::) This axis selects all siblings that come after current element.
     */

    /**
     * 6.Preceding-sibling axis(preceding-sibling::)This axis selects all siblings that come before the current element.
     */

    /**
     * 7.Following axis(following::) This axis selects all elements that come after the current element in the document.
     */

    /**
     * 8.Preceding axis(preceding::)This axis selects all elements that come before the current element in the document.
     */

    /**
     * 9.Self axis(self::)This axis selects the current element.
     */

    /**
     * 10.Namespace axis(namespace::) This axis selects all namespace nodes of the current element.
     */

    /**
     * By combining these axis with various predicates, you can construct xpath expressions that target specific elements
     * or groups of elements in an XML or HTML document.
     */

}
