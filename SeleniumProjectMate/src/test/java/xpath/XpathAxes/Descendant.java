package xpath.XpathAxes;

public class Descendant
{
    /**
     * The descendant axis gives all elements that are descendants of current element.
     *
     * syntax: //currentElement/descendant::*(or)tagName
     *
     *
     */

    /**
     * descendant-or-self:
     * this axis gives elements all descendant elements exclusive current element.
     *
     * syntax: //currentElement/descendant-or-self::*(or)tagName
     */
}
