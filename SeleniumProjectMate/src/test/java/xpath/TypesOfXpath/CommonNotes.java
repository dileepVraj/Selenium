package xpath.TypesOfXpath;

public class CommonNotes
{
    /**
     * Basically we have 2 types of xpath's
     * 1. absolute xpath.
     * 2. relative xpath.
     */

    /**
     * absolute xpath:
     *
     * This type of xpath starts searching for element from root of the html document.
     * We use single forward slash to write absolute xpath.
     */

    /**
     * relative xpath:
     *
     * it can search for an element from any point in html document.
     * This xpath starts with double forward slash, when we're using double slash it stores entire instance of document
     * in it, hence we can query for element that located in any position in document.
     */
}
